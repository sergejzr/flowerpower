package de.l3s.util.alg;

public class ScoredItem {

	private String label;
	private double score;

	public ScoredItem(String label, double score) {
		this.label=label;
		this.score=score;
	}

	public String getLabel() {
		
		return label;
	}

	public double score() {
		return score;
	}

}
